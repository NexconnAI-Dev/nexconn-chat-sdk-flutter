/// Represents the geographic area code for data center selection.
///
/// Used during SDK initialization to specify which regional data center the client should connect to.
enum AreaCode {
  /// Beijing, China data center.
  bj,

  /// Singapore data center.
  sg,

  /// North America data center.
  na,

  /// Saudi Arabia data center.
  sa,

  /// Oman data center.
  om,
}
